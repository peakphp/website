<h1>Creating Peak applications</h1>
<br>
<div class="alert alert-light" role="alert">
    <i>
        Installation is done via composer, a dependency manager for PHP. If it is your first time,
        check this <a href="https://getcomposer.org/doc/00-intro.md">introduction</a> for more information.
    </i>
</div>
<br>

<h4>Install the default application project (<a href="https://github.com/peakphp/peak">peakphp/peak)</a></h4>
<br>
<pre><code class="bash">composer create-project peakphp/peak --prefer-dist</code></pre>
<p>
    The default Peak application project come with preconfigured packages:
    <ul>
        <li>Laravel Database  - <a href="https://laravel.com/docs/5.4/queries">Documentation</a> - <a href="https://github.com/illuminate/database">GitHub</a></li>
        <li>Phinx for database migrations</li>
    </ul>
</p>
